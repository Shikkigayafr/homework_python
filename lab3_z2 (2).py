# Инициализация переменных
total_sum = 0      # сумма всех чисел
count = 0          # количество всех чисел (без учёта нуля)

print("Введите целые числа (для завершения введите 0):")

# Ввод первого числа
num = int(input("Введите число: "))

# Цикл while: работаем, пока число не равно 0
while num != 0:
    total_sum += num    # добавляем число к сумме
    count += 1          # увеличиваем счётчик
    num = int(input("Введите число: "))  # ввод следующего числа

# Вывод результатов
print("\n" + "=" * 40)
print("РЕЗУЛЬТАТЫ:")
print("=" * 40)
print(f"Сумма всех чисел: {total_sum}")
print(f"Количество чисел: {count}")
print("=" * 40)